    @Test
    public void testPacketIn() throws Exception {

        connection.initialize();
        fireReply(6);

        Future<Datapath> dpFuture = connection.datapathsGet("bibi");
        // multi containing the datapaths data
        fireReply();

        Datapath datapath = dpFuture.get();

        assertThat("Datapath was parsed correctly",
                   datapath, is(expectedDatapath()));

        Future<Port> portFuture;

        log.info("Get the internal port by id.");
        portFuture = connection.portsGet(0, datapath);
        fireReply();

        Port port = portFuture.get();

        log.info("Registering ourselves as the notification client");
        portFuture = connection.portsSet(port, datapath);
        fireReply();


        connection.datapathsSetNotificationHandler(datapath, new AbstractNetlinkConnection.Callback<Packet>() {
            @Override
            public void onSuccess(Packet data) {
                log.info("packet {}", data);
            }
        }).get();

        fireReply();
    }

