    @Before
    public void setUp() throws Exception {
        super.setUp(responses);

        connection = OvsDatapathConnection.create(channel, reactor);
    }

    @Test
    public void testPortsGet() throws Exception {

        connection.initialize();

        // fire reply
        fireNewReply();
        fireNewReply();

        Future<Datapath> dpFuture = connection.datapathsGet("test");
        // multi containing the datapaths data
        fireNewReply();

        Datapath datapath = dpFuture.get();

        assertThat("Datapath was parsed correctly",
                   datapath, is(expectedDatapath()));

        Port expectedPort;
        Future<Port> portFuture;

        expectedPort = expectedInternalPort();
        log.info("Get the internal port by name.");
        portFuture = connection.portsGet("internalPort", null);
        fireNewReply();

        assertThat("The returned port should match what we expected",
                   portFuture.get(), is(expectedPort));

        log.info("Get the internal port by id.");
        portFuture = connection.portsGet(1, datapath);
        fireNewReply();

        assertThat("The returned port should match what we expected",
                   portFuture.get(), is(expectedPort));


        expectedPort = expectedGreTunnelPort();
        log.info("Get the gre tunnel port by name.");
        portFuture = connection.portsGet("tunGrePort", null);
        fireNewReply();

        assertThat("The returned port should match what we expected",
                   portFuture.get(), is(expectedPort));

        log.info("Get the gre tunnel port by id.");
        portFuture = connection.portsGet(4, datapath);
        fireNewReply();

        assertThat("The returned port should match what we expected",
                   portFuture.get(), is(expectedPort));

    }

    private Port expectedInternalPort() {
        InternalPort port =
            Ports.newInternalPort("internalPort")
                 .setPortNo(1)
                 .setAddress(new byte[]{
                     (byte) 0xaa, (byte) 0x92, (byte) 0x25,
                     (byte) 0x6c, (byte) 0x43, (byte) 0x2c});

        port.setStats(port.new Stats());
        port.setOptions(port.newOptions());

        return port;
    }

    private Port expectedGreTunnelPort() {
        GreTunnelPort tunGrePort =
            Ports.newGreTunnelPort("tunGrePort")
                 .setPortNo(4)
                 .setAddress(new byte[]{
                     (byte) 0x3a, (byte) 0xef, (byte) 0xa9,
                     (byte) 0xfe, (byte) 0x31, (byte) 0xd3});

        tunGrePort.setStats(tunGrePort.new Stats());
        tunGrePort.setOptions(
            Ports
                .newPortOptions(
                    tunGrePort,
                    new byte[]{
                        (byte) 0xc0, (byte) 0xa8, (byte) 0x64, (byte) 0x01
                    })
                .setSourceIPv4(
                    new byte[]{
                        (byte) 0xc0, (byte) 0xa8, (byte) 0x64, (byte) 0x01
                    }
                )
        );

        return tunGrePort;
    }

