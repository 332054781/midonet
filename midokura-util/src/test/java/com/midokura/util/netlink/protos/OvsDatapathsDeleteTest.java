        connection.initialize();

        fireNewReply();

        fireNewReply();

        Future<Datapath> future = null;

        future = connection.datapathsDelete("test3");
        fireNewReply();

        assertThat("The future was completed",
                   future.isDone(), is(true));
        assertThat("The future was not canceled completed",
                   future.isCancelled(), is(false));

        try {
            future.get();
            assertThat("An exception should have been thrown here", false);
        } catch (ExecutionException e) {
            assertThat(e.getCause(), instanceOf(NetlinkException.class));
            NetlinkException ex = (NetlinkException)e.getCause();

            assertThat("The error code is properly parsed",
                       ex.getErrorCode(), is(19));
        }

        future = connection.datapathsDelete("test");

        fireNewReply();

        Datapath datapath = new Datapath(12, "test");
        datapath.setStats(datapath.new Stats());

        assertThat("We got the proper response",
                   future.get(), is(datapath));
